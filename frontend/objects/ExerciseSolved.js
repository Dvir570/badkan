/**
 * This object is an solved exercise by any student.
 */
class ExerciseSolved {

    /**
     * 
     * @param {string} exercise 
     * @param {grade} grade 
     * @param {string} exerciseId 
     */
    constructor(exercise, grade, exerciseId) {
        this.exercise = exercise;
        this.grade = grade;
        this.exerciseId = exerciseId
    }

}